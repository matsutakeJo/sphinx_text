def add(arg1:int, arg2:int) -> int:
    """足し算

    Args:
        arg1 (int): 1個目
        arg2 (int): 2個目

    Returns:
        int: 合計
    """
    return arg1 + arg2

def mult(arg1:int, arg2:int) -> int:
    """掛け算

    Args:
        arg1 (int): 1個目
        arg2 (int): 2個目

    Returns:
        int: 掛け算
    """
    return arg1 * arg2